    fn stack_pop(&mut self, count: usize) -> Vec<Token<'a>> {
        (0..count)
            .map(|_| {
                let Some(top) = self.stack.pop() else {
                    unreachable!("Stack shouldn't be empty.");
                };
                let Some(_) = self.states.pop() else {
                    unreachable!("States shouldn't be empty.");
                };
                top
            })
            .collect()
    }

